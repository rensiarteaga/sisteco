<?php
/**
 * Nombre de la clase:	cls_DBPeriodo.php
 * Propsito:			Permite ejecutar toda la funcionalidad de la tabla tpm_periodo
 * Autor:				AVQ
 * Fecha creacin:		2009-06-04 9:33:00
 */

class cls_DBPeriodo
{
	var $salida;
	var $query;
	var $var;
	var $nombre_funcion;
	var $codigo_procedimiento;
	var $decodificar;
	
	function __construct()
	{
		$this->decodificar=$decodificar;
	}
	
	/**
	 * Nombre de la funcin:	ListarPeriodo
	 * Propsito:				Desplegar los registros de tpm_periodo
	 * Autor:				    AVQ
	 * Fecha de creacin:		2009-06-04 9:33:00
	 */
	function ListarPeriodo($cant,$puntero,$sortcol,$sortdir,$criterio_filtro,$id_financiador,$id_regional,$id_programa,$id_proyecto,$id_actividad)
	{
		$this->salida = "";
		$this->nombre_funcion = 'f_tpm_periodo_sel';
		$this->codigo_procedimiento = "'PM_PERIOD_SEL'";

		$func = new cls_funciones();//Instancia de las funciones generales
		
		//Instancia la clase middle para la ejecucin de la funcin de la BD
		$this->var = new cls_middle($this->nombre_funcion,$this->codigo_procedimiento);

		//Carga los parmetros del filtro
		$this->var->cant = $cant;
		$this->var->puntero = $puntero;
		$this->var->sortcol = "'$sortcol'";
		$this->var->sortdir = "'$sortdir'";
		$this->var->criterio_filtro = "'$criterio_filtro'";

		//Carga los parmetros especficos de la estructura programtica
		$this->var->add_param($func->iif($id_financiador == '',"'%'","'$id_financiador'"));//id_financiador
		$this->var->add_param($func->iif($id_regional == '',"'%'","'$id_regional'"));//id_regional
		$this->var->add_param($func->iif($id_programa == '',"'%'","'$id_programa'"));//id_programa
		$this->var->add_param($func->iif($id_proyecto == '',"'%'","'$id_proyecto'"));//id_proyecto
		$this->var->add_param($func->iif($id_actividad == '',"'%'","'$id_actividad'"));//id_actividad

		//Carga la definicin de columnas con sus tipos de datos
		$this->var->add_def_cols('id_periodo',' integer'); 
  		$this->var->add_def_cols('id_gestion','INTEGER'); 
  		$this->var->add_def_cols('periodo',' NUMERIC(2,0)'); 
  		$this->var->add_def_cols('fecha_inicio',' DATE'); 
  		$this->var->add_def_cols('fecha_final',' DATE '); 
  		$this->var->add_def_cols('estado_peri_gral',' VARCHAR(15)');
  		$this->var->add_def_cols('periodo_lite',' VARCHAR(15)');
		
		//Ejecuta la funcin de consulta
		$res = $this->var->exec_query();

		//Obtiene el array de salida de la funcin y retorna el resultado de la ejecucin
		$this->salida = $this->var->salida;

		//Obtiene la cadena con que se llam a la funcin de postgres
		$this->query = $this->var->query;
		
		return $res;
	}
	
	/**
	 * Nombre de la funcin:	ContarPeriodo
	 * Propsito:				Contar los registros de tpm_periodo
	 * Autor:				    AVQ
	 * Fecha de creacin:		2009-06-04 9:33:00
	 */
	function ContarPeriodo($cant,$puntero,$sortcol,$sortdir,$criterio_filtro,$id_financiador,$id_regional,$id_programa,$id_proyecto,$id_actividad)
	{
		$this->salida = "";
		$this->nombre_funcion = 'f_tpm_periodo_sel';
		$this->codigo_procedimiento = "'PM_PERIOD_COUNT'";

		$func = new cls_funciones();//Instancia de las funciones generales
		
		//Instancia la clase middle para la ejecucin de la funcin de la BD
		$this->var = new cls_middle($this->nombre_funcion,$this->codigo_procedimiento);

		//Carga los parmetros del filtro
		$this->var->cant = $cant;
		$this->var->puntero = $puntero;
		$this->var->sortcol = "'$sortcol'";
		$this->var->sortdir = "'$sortdir'";
		$this->var->criterio_filtro = "'$criterio_filtro'";

		//Carga los parmetros especficos de la estructura programtica
		$this->var->add_param($func->iif($id_financiador == '',"'%'","'$id_financiador'"));//id_financiador
		$this->var->add_param($func->iif($id_regional == '',"'%'","'$id_regional'"));//id_regional
		$this->var->add_param($func->iif($id_programa == '',"'%'","'$id_programa'"));//id_programa
		$this->var->add_param($func->iif($id_proyecto == '',"'%'","'$id_proyecto'"));//id_proyecto
		$this->var->add_param($func->iif($id_actividad == '',"'%'","'$id_actividad'"));//id_actividad

		
		//Carga la definicin de columnas con sus tipos de datos
		$this->var->add_def_cols('total','bigint');

		//Ejecuta la funcin de consulta
		$res = $this->var->exec_query();

		//Obtiene el array de salida de la funcin
		$this->salida = $this->var->salida;

		//Si la ejecucin fue satisfactoria modifica la salida para que solo devuelva el total de la consulta
		if($res)
		{
			$this->salida = $this->var->salida[0][0];
		}

		//Obtiene la cadena con que se llam a la funcin de postgres
		$this->query = $this->var->query;

		//Retorna el resultado de la ejecucin
		return $res;
	}
	
	
}?>