<?php
/**
 * Nombre de la Clase:	cls_DBMedida
 * Propsito:			Permite ejecutar la funcionalidad de la tabla tpm_medida
 * Autor:				Rodrigo Chumacero Moscoso
 * Fecha creacin:		27-06-2007
 *
 */
class cls_DBMedida
{
	//Variable que contiene la salida de la ejecucin de la funcin
	//si la funcin tuvo error (false), salida contendr el mensaje de error
	//si la funcin no tuvo error (true), salida contendr el resultado, ya sea un conjunto de datos o un mensaje de confirmacin
	var $salida;
	
	//Variable que contedr la cadena de llamada a las funciones postgres
	var $query;

	//Variables para la ejecucin de funciones
	var $var; //middle_client
	var $nombre_funcion; //nombre de la funcin a ejecutar
	var $codigo_procedimiento; //codigo del procedimiento a ejecutar

	//Nombre del archivo
	var $nombre_archivo = "cls_DBMedida.php";

	//Matriz de parmetros de validacin de todas las columnas
	var $matriz_validacion = array();
	
	//Bandera que indica si los datos se decodificarn o no
	var $decodificar = false;

	function __construct($decodificar)
	{
		//Carga los parmetro de validacin de todas las columnas
		//$this->cargar_param_valid();
		
		//Carga en una variable interna la bandera del GET o POST
		$this->decodificar = $decodificar;
	}

	/**
	 * Nombre de la funcin:	ListarMedida
	 * Propsito:				Desplegar los registros de tpm_medida en funcin de los parmetros del filtro
	 * Autor:					Rodrigo Chumacero Moscoso
	 * Fecha de creacin:		27-06-2007
	 *
	 * @param unknown_type $cant
	 * @param unknown_type $puntero
	 * @param unknown_type $sortcol
	 * @param unknown_type $sortdir
	 * @param unknown_type $criterio_filtro
	 * @param unknown_type $id_financiador
	 * @param unknown_type $id_regional
	 * @param unknown_type $id_programa
	 * @param unknown_type $id_proyecto
	 * @param unknown_type $id_actividad
	 * @return unknown
	 */
	function ListarMedida($cant,$puntero,$sortcol,$sortdir,$criterio_filtro,$id_financiador,$id_regional,$id_programa,$id_proyecto,$id_actividad)
	{
		$this->salida = "";
		$this->nombre_funcion = 'f_tpm_medida_consultas';
		$this->codigo_procedimiento = "'PM_MED_SEL'";

		$func = new cls_funciones();//Instancia de las funciones generales

		//Instancia la clase midlle para la ejecucin de la funcin de la BD
		$this->var = new cls_middle($this->nombre_funcion,$this->codigo_procedimiento);

		//Carga los parmetros del filtro
		$this->var->cant = $cant;
		$this->var->puntero = $puntero;
		$this->var->sortcol = "'$sortcol'";
		$this->var->sortdir = "'$sortdir'";
		$this->var->criterio_filtro = "'$criterio_filtro'";

		//Carga los parmetros especficos de la estructura programtica
		$this->var->add_param($func->iif($id_financiador == '','NULL',$id_financiador));//id_financiador
		$this->var->add_param($func->iif($id_regional == '','NULL',$id_regional));//id_regional
		$this->var->add_param($func->iif($id_programa == '','NULL',$id_programa));//id_programa
		$this->var->add_param($func->iif($id_proyecto == '','NULL',$id_proyecto));//id_proyecto
		$this->var->add_param($func->iif($id_actividad == '','NULL',$id_actividad));//id_actividad

		//Carga la definicin de columnas con sus tipos de datos
		$this->var->add_def_cols('id_medida','integer');
		$this->var->add_def_cols('descripcion','varchar');
		$this->var->add_def_cols('medida_princ','varchar');
		$this->var->add_def_cols('equiv_medida_princ','real');
		$this->var->add_def_cols('id_medida_tipo','integer');
		$this->var->add_def_cols('desc_medida_tipo','varchar');

		//Ejecuta la funcin de consulta
		$res = $this->var->exec_query();

		//Obtiene el array de salida de la funcin y retorna el resultado de la ejecucin
		$this->salida = $this->var->salida;
		
		//Obtiene la cadena con que se llam a la funcin de postgres
		$this->query = $this->var->query;
		return $res;
	}

	/**
	 * Nombre de la funcin:	ContarListaMedida
	 * Propsito:				Contar el total de registros desplegados en funcin de los parmetros de filtro
	 * Autor:					Rodrigo Chumacero Moscoso
	 * Fecha de creacin:		27-06-2007
	 *
	 * @param unknown_type $cant
	 * @param unknown_type $puntero
	 * @param unknown_type $sortcol
	 * @param unknown_type $sortdir
	 * @param unknown_type $criterio_filtro
	 * @param unknown_type $id_financiador
	 * @param unknown_type $id_regional
	 * @param unknown_type $id_programa
	 * @param unknown_type $id_proyecto
	 * @param unknown_type $id_actividad
	 * @return unknown
	 */
	function ContarListaMedida($cant,$puntero,$sortcol,$sortdir,$criterio_filtro,$id_financiador,$id_regional,$id_programa,$id_proyecto,$id_actividad)
	{
		$this->salida = "";
		$this->nombre_funcion = 'f_tpm_medida_consultas';
		$this->codigo_procedimiento = "'PM_MED_SEL_COUNT'";

		$func = new cls_funciones();//Instancia de las funciones generales

		//Instancia la clase midlle para la ejecucin de la funcin de la BD
		$this->var = new cls_middle($this->nombre_funcion,$this->codigo_procedimiento);

		//Carga los parmetros del filtro
		$this->var->cant = $cant;
		$this->var->puntero = $puntero;
		$this->var->sortcol = "'$sortcol'";
		$this->var->sortdir = "'$sortdir'";
		$this->var->criterio_filtro = "'$criterio_filtro'";

		//Carga los parmetros especficos de la estructura programtica
		$this->var->add_param($func->iif($id_financiador == '','NULL',$id_financiador));//id_financiador
		$this->var->add_param($func->iif($id_regional == '','NULL',$id_regional));//id_regional
		$this->var->add_param($func->iif($id_programa == '','NULL',$id_programa));//id_programa
		$this->var->add_param($func->iif($id_proyecto == '','NULL',$id_proyecto));//id_proyecto
		$this->var->add_param($func->iif($id_actividad == '','NULL',$id_actividad));//id_actividad

		//Carga la definicin de columnas con sus tipos de datos
		$this->var->add_def_cols('total','bigint');

		//Ejecuta la funcin de consulta
		$res = $this->var->exec_query();

		//Obtiene el array de salida de la funcin
		$this->salida = $this->var->salida;

		//Si la ejecucin fue satisfactoria modifica la salida para que solo devuelva el total de la consulta
		if($res)
		{
			$this->salida = $this->var->salida[0][0];
		}
		
		//Obtiene la cadena con que se llam a la funcin de postgres
		$this->query = $this->var->query;

		//Retorna el resultado de la ejecucin
		return $res;
	}

}?>